package c.inheritance;

public class ParentCasting{
	public ParentCasting(){
		System.out.println("ParentCasting Constructor");
	}

	public ParentCasting(String name){
		System.out.println("ParentCasting (" + name + ") Constructor");
	}

	public void printName(){
		System.out.println("ParentCasting printName()");
	}	

}